      WRITE(IOCODE,1100) N,QWAQR(N),CUMAQR(N)
 1100 FORMAT(1X,'AQUIFER MODEL FOR ROCK REGION ',I3,':',
     & /1X,'AQUIFER INFLUX RATE (STB/D) =',F9.1,
     & 3X,'CUM. AQUIFER INFLUX (STB)    =',E10.4,//)
 2000 CONTINUE
      RETURN
      END
